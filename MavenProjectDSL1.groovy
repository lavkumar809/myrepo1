job('First-Maven-Project-Via-DSLjob') {
    logRotator(-1, 1)
    description("First maven job generated by the DSL on ${new Date()}, the project is a maven project hosted on gitlab")

    scm {
        git("https://github.com/lavkumar809/myrepo1.git", "master")
    }

    triggers {
        scm('* * * * *')
    }

    steps {
        maven('Maven-3.9.2') {  // Specify the Maven version
            goals('clean package')
            mavenInstallation('Maven-3.9.2')  // The name of the Maven installation configured in Jenkins
            pom('pom.xml')
        }
    }

    publishers {
        // Archive the war file generated
        archiveArtifacts '**/*.jar'
    }
}
